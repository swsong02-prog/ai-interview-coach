def _match_group(job_role: str):
    if not job_role:
        return None
    low = job_role.lower()
    for group_name, info in JOB_GROUPS.items():
        for kw in info["keywords"]:
            if kw in low:
                return group_name
    return None


def _normalize_level(level: str) -> str:
    """입력된 난이도를 '하/중/상' 중 하나로 정규화."""
    if not level:
        return "중"
    s = str(level).strip().lower()
    if s in ("하", "1", "easy", "low", "기초"):
        return "하"
    if s in ("상", "3", "hard", "high", "심화"):
        return "상"
    return "중"


def _compose(pool, n):
    """자기소개를 맨 앞에 두고 pool에서 무작위로 채워 n개 구성."""
    questions = [INTRO_QUESTION]
    remain = [q for q in pool if q != INTRO_QUESTION]
    random.shuffle(remain)
    questions.extend(remain[: max(0, n - 1)])
    # 모자라면 다른 난이도/공통에서 보충
    if len(questions) < n:
        backup = []
        for lv in LEVELS:
            backup.extend(COMMON_BY_LEVEL[lv])
        backup = [q for q in backup if q not in questions]
        random.shuffle(backup)
        questions.extend(backup[: n - len(questions)])
    return questions[:n]


