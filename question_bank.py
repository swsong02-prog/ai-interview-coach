def build_questions_from_resume(resume_text: str, job_role: str = "일반 직무",
                                level: str = "중", n: int = 5):
    """
    자소서 기반 질문 구성. 자소서 생성이 실패하면 직무/공통 질문으로 자동 폴백.
    """
    level = _normalize_level(level)
    if resume_text and resume_text.strip():
        print("[질문] 자소서를 분석해 맞춤 질문을 생성 중입니다... (잠시 걸릴 수 있어요)")
        qs = _generate_from_resume(resume_text, job_role, level, n)
        if qs:
            print("[질문] 자소서 기반 맞춤 질문을 생성했습니다.")
            return qs[:n]
    # 폴백: 기존 직무/난이도 로직
    print("[질문] 자소서 질문 생성을 건너뛰고 일반 질문으로 진행합니다.")
    return build_questions(job_role=job_role, level=level, n=n)


def build_questions(job_role: str = "일반 직무", level: str = "중",
                    n: int = 5, use_ai: bool = True, group: str = None):
    """
    직무 + 난이도에 맞는 면접 질문 n개 구성.
    첫 질문은 항상 자기소개. 우선순위: 직무군 매칭 > Ollama 생성 > 공통.
    :param group: 직군명을 직접 지정(메뉴 선택). 주어지면 키워드 매칭을 건너뜀.
    """
    level = _normalize_level(level)
    # group이 직접 주어지고 실제 존재하면 그대로 사용, 아니면 키워드 매칭
    if group is not None and group in JOB_GROUPS:
        matched = group
    else:
        matched = _match_group(job_role)

    # 1순위: 미리 정의된 직무군 + 난이도
    if matched is not None:
        print(f"[질문] '{job_role}' → '{matched}' 직무군 · 난이도 '{level}' 전문 질문 사용.")
        return _compose(JOB_GROUPS[matched][level], n)

    # 2순위: Ollama 로 직무·난이도 맞춤 생성
    if use_ai and job_role and job_role != "일반 직무":
        print(f"[질문] '{job_role}'(난이도 {level}) 맞춤 질문을 AI로 생성 중... (잠시 걸릴 수 있어요)")
        ai_qs = _generate_with_ollama(job_role, level, n)
        if ai_qs:
            print(f"[질문] AI가 '{job_role}'(난이도 {level}) 맞춤 질문을 생성했습니다.")
            return ai_qs[:n]

    # 3순위: 공통 질문(난이도별)
    print(f"[질문] 공통 면접 질문(난이도 {level})으로 진행합니다.")
    return _compose(COMMON_BY_LEVEL[level], n)


