def read_resume(path: str) -> str:
    """
    자소서 파일(.txt / .docx)을 읽어 텍스트로 반환. 실패하면 빈 문자열.
    """
    if not path:
        return ""
    path = path.strip().strip('"').strip("'")   # 따옴표/공백 제거
    if not os.path.exists(path):
        print(f"[자소서] 파일을 찾을 수 없습니다: {path}")
        return ""
    ext = os.path.splitext(path)[1].lower()
    try:
        if ext == ".docx":
            try:
                import docx   # python-docx
            except Exception:
                print("[자소서] 워드(.docx)를 읽으려면 python-docx가 필요합니다:  pip install python-docx")
                return ""
            doc = docx.Document(path)
            return "\n".join(p.text for p in doc.paragraphs if p.text.strip())
        elif ext in (".txt", ".md"):
            for enc in ("utf-8", "cp949", "euc-kr"):
                try:
                    with open(path, encoding=enc) as f:
                        return f.read().strip()
                except UnicodeDecodeError:
                    continue
            print("[자소서] 파일 인코딩을 읽지 못했습니다. UTF-8로 저장해 주세요.")
            return ""
        else:
            print(f"[자소서] 지원하지 않는 형식입니다({ext}). .txt 또는 .docx 를 사용하세요.")
            return ""
    except Exception as e:
        print(f"[자소서] 파일 읽기 실패: {e}")
        return ""


