def _match_group(job_role: str):
    if not job_role:
        return None
    low = job_role.lower()
    for group_name, info in JOB_GROUPS.items():
        for kw in info["keywords"]:
            if kw in low:
                return group_name
    return None


def _normalize_level(level: str) -> str:
    """입력된 난이도를 '하/중/상' 중 하나로 정규화."""
    if not level:
        return "중"
    s = str(level).strip().lower()
    if s in ("하", "1", "easy", "low", "기초"):
        return "하"
    if s in ("상", "3", "hard", "high", "심화"):
        return "상"
    return "중"


def _compose(pool, n):
    """자기소개를 맨 앞에 두고 pool에서 무작위로 채워 n개 구성."""
    questions = [INTRO_QUESTION]
    remain = [q for q in pool if q != INTRO_QUESTION]
    random.shuffle(remain)
    questions.extend(remain[: max(0, n - 1)])
    # 모자라면 다른 난이도/공통에서 보충
    if len(questions) < n:
        backup = []
        for lv in LEVELS:
            backup.extend(COMMON_BY_LEVEL[lv])
        backup = [q for q in backup if q not in questions]
        random.shuffle(backup)
        questions.extend(backup[: n - len(questions)])
    return questions[:n]


def _generate_with_ollama(job_role: str, level: str, n: int):
    """Ollama 로 직무·난이도 맞춤 질문 생성. 실패하면 None."""
    try:
        import ollama
    except Exception:
        return None
    need = n - 1
    level_hint = {
        "하": "면접 경험이 적은 지원자를 위한 기초적이고 부담 없는 질문",
        "중": "일반적인 신입 채용 수준의 실무 경험을 묻는 질문",
        "상": "전공 지식과 깊은 사고를 요구하는 심화·압박 질문",
    }.get(level, "실무 수준의 질문")
    prompt = f"""당신은 '{job_role}' 직무의 채용 면접관입니다.
난이도는 '{level}'({level_hint})입니다.

이 직무·난이도에 맞는 면접 질문 {need}개를 만들어 주세요.

규칙:
- 모두 한국어로 작성합니다.
- '{level}' 난이도에 맞는 수준으로 만듭니다.
- 자기소개 같은 일반 질문은 제외하고 직무 특화 질문으로 만듭니다.
- 아래 JSON 형식 하나만 출력하세요. 다른 텍스트 금지.

{{"questions": ["질문1", "질문2", "질문3", "질문4"]}}"""
    try:
        resp = ollama.chat(
            model="qwen2.5:7b",
            messages=[
                {"role": "system", "content": "당신은 한국어로만 답하는 전문 채용 면접관입니다."},
                {"role": "user", "content": prompt},
            ],
            format="json",
            options={"temperature": 0.7},
        )
        content = resp["message"]["content"]
        start, end = content.find("{"), content.rfind("}")
        data = json.loads(content[start:end + 1])
        qs = [str(q).strip() for q in data.get("questions", []) if str(q).strip()]
        if not qs:
            return None
        return [INTRO_QUESTION] + qs[:need]
    except Exception as e:
        print(f"[안내] AI 질문 생성 실패 → 공통 질문으로 진행합니다. (사유: {e})")
        return None


def build_questions(job_role: str = "일반 직무", level: str = "중",
                    n: int = 5, use_ai: bool = True):
    """
    직무 + 난이도에 맞는 면접 질문 n개 구성.
    첫 질문은 항상 자기소개. 우선순위: 직무군 매칭 > Ollama 생성 > 공통.
    """
    level = _normalize_level(level)
    group = _match_group(job_role)

    # 1순위: 미리 정의된 직무군 + 난이도
    if group is not None:
        print(f"[질문] '{job_role}' → '{group}' 직무군 · 난이도 '{level}' 전문 질문 사용.")
        return _compose(JOB_GROUPS[group][level], n)

    # 2순위: Ollama 로 직무·난이도 맞춤 생성
    if use_ai and job_role and job_role != "일반 직무":
        print(f"[질문] '{job_role}'(난이도 {level}) 맞춤 질문을 AI로 생성 중... (잠시 걸릴 수 있어요)")
        ai_qs = _generate_with_ollama(job_role, level, n)
        if ai_qs:
            print(f"[질문] AI가 '{job_role}'(난이도 {level}) 맞춤 질문을 생성했습니다.")
            return ai_qs[:n]

    # 3순위: 공통 질문(난이도별)
    print(f"[질문] 공통 면접 질문(난이도 {level})으로 진행합니다.")
    return _compose(COMMON_BY_LEVEL[level], n)


